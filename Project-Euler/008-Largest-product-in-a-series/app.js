/**
 * Find the largest consecutive number possible.
 * 
 * @param {String} string Large string of numbers (JS breaks if numbers are too big
 * @param {Number} sequence How many consecutive are need in  a row
 */

var largestSequence = function(string, sequence) {
    let largestSum = 0, i, length = string.length;
    let largestPossible = Math.pow(9, sequence);

    for(i = 0; i < length - sequence; i+=1) {
        let tempNum = 1, j;

        for(j = 0; j < sequence; j+=1) {
            tempNum *= +string[j + i];
        }
        
        if(tempNum > largestSum) {
            largestSum = tempNum;
        }

        if(largestSum === largestPossible) {
            return largestSum;
        }
    }

    return largestSum;
}

